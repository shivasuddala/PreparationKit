export const getQuestionsByLevel = (questions, level) => {
  if (level === 'all') return questions;
  return questions.filter(q => q.level === level);
};

export const searchQuestions = (searchTerm) => {
  const results = [];
  topics.forEach(topic => {
    topic.subtopics.forEach(subtopic => {
      subtopic.questions.forEach(question => {
        if (
          question.question.toLowerCase().includes(searchTerm.toLowerCase()) ||
          question.shortAnswer.toLowerCase().includes(searchTerm.toLowerCase()) ||
          question.tags.some(tag => tag.toLowerCase().includes(searchTerm.toLowerCase()))
        ) {
          results.push({ ...question, topic: topic.name, subtopic: subtopic.name });
        }
      });
    });
  });
  return results;
};

